    <table class="checklist">
        <tr><td style="width:20px;"><span class="box"></span></td><td><span class="item-text">Read your lease's cleaning clause</span><br><span class="item-note">Know exactly what standard you're expected to leave the property in — "broom clean" and "professionally cleaned" are not the same thing.</span></td></tr>
        <tr><td><span class="box"></span></td><td><span class="item-text">Take dated photos of every room before you move anything out</span><br><span class="item-note">This is your evidence if there's ever a dispute over pre-existing condition.</span></td></tr>
        <tr><td><span class="box"></span></td><td><span class="item-text">Request a joint inspection with your landlord or agent, if possible</span><br><span class="item-note">Not always offered, but worth asking for — it removes ambiguity about what was found and when.</span></td></tr>
        <tr><td><span class="box"></span></td><td><span class="item-text">Keep your professional cleaning invoice/receipt</span><br><span class="item-note">Proof of a professional clean is strong evidence in a deposit dispute.</span></td></tr>
    </table>

    <div class="section-title">Kitchen</div>
    <table class="checklist">
        <tr><td style="width:20px;"><span class="box"></span></td><td><span class="item-text">Oven, hob and extractor degreased inside and out</span></td></tr>
        <tr><td><span class="box"></span></td><td><span class="item-text">Fridge/freezer emptied, cleaned inside, and left switched off with the door ajar</span></td></tr>
        <tr><td><span class="box"></span></td><td><span class="item-text">All cupboards and drawers wiped inside and out</span></td></tr>
        <tr><td><span class="box"></span></td><td><span class="item-text">Countertops, splashback and sink descaled</span></td></tr>
        <tr><td><span class="box"></span></td><td><span class="item-text">Floors mopped, skirting wiped</span></td></tr>
    </table>

    <div class="section-title">Bathrooms</div>
    <table class="checklist">
        <tr><td style="width:20px;"><span class="box"></span></td><td><span class="item-text">Toilet, shower, bath and basin fully descaled</span></td></tr>
        <tr><td><span class="box"></span></td><td><span class="item-text">Grout and sealant checked for mould, cleaned or flagged if damaged</span></td></tr>
        <tr><td><span class="box"></span></td><td><span class="item-text">Mirrors and glass streak-free</span></td></tr>
        <tr><td><span class="box"></span></td><td><span class="item-text">Extractor fan dusted</span></td></tr>
