<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">Where they still fall short</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    On heavy limescale, baked-on oven grease, or mould that's had time to set into grout, plant-based formulas typically need more contact time and more elbow grease than a targeted chemical treatment. For a light maintenance clean, that trade-off barely matters. For a first deep clean in years, it can mean the difference between one pass and three.
</p>

<x-blog.callout label="Our approach">
    We default to eco-friendly, biodegradable products across every job — they handle the vast majority of household cleaning perfectly well. For specific problem areas like heavy limescale or mould, we use a targeted, properly diluted treatment only where it's needed, rather than blanket-applying harsher chemicals across an entire home.
</x-blog.callout>

