<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">1. The oven hasn't been properly cleaned in months</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    If you can smell burnt-on grease every time the oven heats up, baked-on residue has built past the point a household cleaner can shift. This is one of the most requested single-room jobs we do.
</p>

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">2. Grout and shower glass have gone grey</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    Limescale and soap scum harden over time. Once grout looks permanently grey no matter how hard you scrub, it needs a proper descale treatment rather than another bottle of bathroom spray.
</p>

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">3. Dust is visible on top of cupboards and skirting boards</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    These are the areas a regular wipe-down never reaches. In coastal areas like Blouberg and Big Bay, sea air and sand accelerate this build-up noticeably faster than inland suburbs.
</p>

