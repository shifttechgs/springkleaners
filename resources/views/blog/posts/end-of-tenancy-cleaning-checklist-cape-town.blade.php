<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">Why deposits get docked</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    Most deductions have nothing to do with damage — they're cleaning related. Built-up oven grease, limescale in showers, dust on top of cupboards, and marks on skirting boards are the most common culprits we see when we're called in to fix a failed inspection before the final walkthrough.
</p>

<x-blog.callout label="Good to know">
    Most Cape Town letting agents use a standard inventory report from move-in day. Anything that doesn't match "as found" condition — including cleanliness — can be charged against your deposit at the landlord's discretion.
</x-blog.callout>

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">The room-by-room checklist</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">Kitchen:</p>
<x-blog.checklist :items="[
    'Oven, hob and extractor fan degreased inside and out',
    'Inside of fridge and freezer wiped down and defrosted',
    'Cupboards emptied and wiped, including tops and inside corners',
    'Sink, taps and splashback descaled',
]" />

<p class="text-muted text-[15.5px] leading-[1.85] mb-5">Bathrooms:</p>
<x-blog.checklist :items="[
    'Shower glass, tiles and grout descaled',
    'Toilet base, cistern and behind the bowl cleaned',
    'Extractor fan cover dusted',
    'Mirrors and cabinets streak-free',
]" />

<p class="text-muted text-[15.5px] leading-[1.85] mb-5">Living areas and bedrooms:</p>
<x-blog.checklist :items="[
    'Skirting boards, light switches and door frames wiped',
    'Windows and sills cleaned inside',
    'Built-in cupboards emptied, vacuumed and wiped',
    'Carpets professionally cleaned or hard floors sealed and polished',
]" />

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">When to book the clean</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    Book your end-of-tenancy clean for the day after your furniture leaves, ideally 24–48 hours before the final inspection. This gives enough buffer to fix anything the agent flags without paying for a rushed same-day callout.
</p>

