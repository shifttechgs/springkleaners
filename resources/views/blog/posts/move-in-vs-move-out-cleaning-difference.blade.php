<p class="text-navy/80 text-[17px] leading-[1.8] font-medium mb-8">
    They're often quoted as the same service, but move-in and move-out cleans solve different problems. Booking the wrong one means paying for work you don't actually need — or missing the part that matters most for your situation.
</p>

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">Move-out cleaning: proving the condition you're leaving behind</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    This is about the inspection report and your deposit. Every surface needs to match — or beat — the condition recorded when you moved in. The priority is documentable cleanliness: ovens, grout, cupboard interiors, skirtings, and anything an agent's checklist will scrutinise.
</p>

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">Move-in cleaning: starting somewhere that's actually yours</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    Even a professionally cleaned handover was cleaned for someone else's standards, not yours — and empty homes gather dust fast while sitting vacant between tenants. Move-in cleans focus on sanitising surfaces you'll touch daily: kitchen counters, bathroom fixtures, cupboard interiors, and floors throughout.
</p>

<x-blog.callout label="The key difference">
    Move-out cleaning is inspection-driven and documentation-focused. Move-in cleaning is comfort-driven and sanitation-focused. The scope of work overlaps, but what "done well" looks like is different for each.
</x-blog.callout>

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">What both should always include</h2>
<x-blog.checklist :items="[
    'Full kitchen — oven, extractor, cupboards inside and out',
    'Bathrooms fully descaled, including grout and behind the toilet',
    'Windows, sills and skirting boards throughout',
    'Floors vacuumed, mopped or professionally carpet-cleaned',
]" />

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">Which one do you need?</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    If you're handing back keys and a deposit is on the line, book a move-out clean and time it right before the final inspection. If you've just been handed keys to a new place, book a move-in clean before your furniture arrives — it's far easier to clean an empty room than one full of boxes.
</p>

