<p class="text-navy/80 text-[17px] leading-[1.8] font-medium mb-8">
    Under most standard South African lease agreements, tenants are expected to return a rented property in the same clean condition it was received in — fair wear and tear aside — and landlords can typically deduct reasonable cleaning costs from the deposit if it isn't. This is general guidance, not legal advice: always check your specific lease for the exact clause.
</p>

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">The general principle</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    South African rental law generally distinguishes between normal wear and tear (which a landlord can't charge a tenant for) and genuine neglect or damage (which they can). A property left dirty, greasy, or with built-up grime beyond what normal daily living would cause usually falls on the tenant's side of that line — which is why cleaning is one of the most common line items in a deposit deduction.
</p>

