    Renovation dust doesn't behave like household dust. It's fine, it's everywhere, and it settles back out of the air for days after the builders pack up. A proper post-construction clean is a different job to a standard deep clean — here's what it actually involves.
</p>

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">Why builder dust needs a different approach</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5">
    Cement and plaster dust is abrasive and static-charged, which means it clings to walls, light fittings and vents rather than settling neatly on the floor. Cleaning it with a standard mop-and-wipe approach just moves it around. Post-construction cleans use a specific top-to-bottom sequence and dust-rated equipment to actually remove it.
</p>

<h2 class="text-2xl md:text-[28px] font-extrabold text-navy tracking-tight mt-12 mb-4">The three phases</h2>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5"><strong class="text-navy">Phase 1 — Rough clean:</strong> Bulk debris, packaging, tape residue and protective film removed. Vents and light fittings vacuumed first so dust falls before surfaces are wiped.</p>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5"><strong class="text-navy">Phase 2 — Detail clean:</strong> Every surface wiped down including skirtings, window frames, cupboard interiors and behind appliances. Paint splatter removed from tiles, glass and fittings.</p>
<p class="text-muted text-[15.5px] leading-[1.85] mb-5"><strong class="text-navy">Phase 3 — Final polish:</strong> Floors sealed or polished, glass and mirrors streak-free, all surfaces move-in ready.</p>

<x-blog.callout label="Timing tip">
    Book your post-construction clean for 24 hours after the last trade leaves site — painters and electricians often generate a final round of dust that's worth waiting out.
</x-blog.callout>

